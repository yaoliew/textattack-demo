"""
Print attack results summary (metrics table + confusion matrix) from TextAttack
JSON result files.
"""

import argparse
import glob
import json
import os

from sklearn.metrics import confusion_matrix as sk_confusion_matrix


def load_results(path):
    """Load attack results from a JSON file."""
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def compute_metrics_from_json(entries):
    """
    Compute metrics from JSON entries generated by test_qwen_smishing.py.
    """
    num_successful = sum(1 for e in entries if e.get("successful") is True)
    num_failed = sum(1 for e in entries if e.get("result_type") == "FailedAttackResult")
    num_skipped = sum(
        1 for e in entries if e.get("result_type") == "SkippedAttackResult" or "error" in e
    )

    with_orig_pred = [
        e for e in entries if "original_prediction" in e and e["original_prediction"] is not None
    ]
    if with_orig_pred:
        original_correct = sum(1 for e in with_orig_pred if e["original_label"] == e["original_prediction"])
        original_accuracy = original_correct / len(with_orig_pred) * 100
    else:
        original_accuracy = 0.0

    with_pert_pred = [
        e for e in entries if "perturbed_prediction" in e and e["perturbed_prediction"] is not None
    ]
    if with_pert_pred:
        attacked_correct = sum(1 for e in with_pert_pred if e["original_label"] == e["perturbed_prediction"])
        attacked_accuracy = attacked_correct / len(with_pert_pred) * 100
    else:
        attacked_accuracy = 0.0

    # Recall/F1 on attacked predictions for the positive class (1 = Smishing).
    # These are 0.0 when there is no positive support in y_true.
    if with_pert_pred:
        y_true = [e["original_label"] for e in with_pert_pred]
        y_pred = [e["perturbed_prediction"] for e in with_pert_pred]
        cm = sk_confusion_matrix(y_true, y_pred, labels=[0, 1])
        tp = int(cm[1][1])
        fn = int(cm[1][0])
        fp = int(cm[0][1])
        recall = (tp / (tp + fn) * 100) if (tp + fn) > 0 else 0.0
        f1 = (2 * tp / (2 * tp + fp + fn) * 100) if (2 * tp + fp + fn) > 0 else 0.0
    else:
        recall = 0.0
        f1 = 0.0

    total_attempted = num_successful + num_failed
    attack_success_rate = (num_successful / total_attempted * 100) if total_attempted > 0 else 0.0

    perturbed_pcts = []
    word_counts = []
    for e in entries:
        if "error" in e:
            continue
        orig = e.get("original_text") or ""
        pert = e.get("perturbed_text") or orig
        orig_words = orig.split()
        pert_words = pert.split()
        if not orig_words:
            continue
        word_counts.append(len(orig_words))
        min_len = min(len(orig_words), len(pert_words))
        num_changed = sum(1 for i in range(min_len) if orig_words[i] != pert_words[i])
        num_changed += abs(len(pert_words) - len(orig_words))
        pct = (num_changed / len(orig_words)) * 100
        perturbed_pcts.append(pct)
    avg_perturbed_word_pct = sum(perturbed_pcts) / len(perturbed_pcts) if perturbed_pcts else 0.0

    if not word_counts:
        word_counts = [len((e.get("original_text") or "").split()) for e in entries if "original_text" in e]
    avg_words_per_input = sum(word_counts) / len(word_counts) if word_counts else 0.0

    # Not stored in JSON; keep for display compatibility.
    avg_num_queries = 0.0

    return {
        "num_successful": num_successful,
        "num_failed": num_failed,
        "num_skipped": num_skipped,
        "original_accuracy": original_accuracy,
        "attacked_accuracy": attacked_accuracy,
        "attack_success_rate": attack_success_rate,
        "recall": recall,
        "f1": f1,
        "avg_perturbed_word_pct": avg_perturbed_word_pct,
        "avg_words_per_input": avg_words_per_input,
        "avg_num_queries": avg_num_queries,
    }


def confusion_matrix_from_json(entries):
    """
    Return (y_true, y_pred) for confusion matrix using perturbed predictions.
    """
    with_pert = [e for e in entries if "perturbed_prediction" in e and e["perturbed_prediction"] is not None]
    y_true = [e["original_label"] for e in with_pert]
    y_pred = [e["perturbed_prediction"] for e in with_pert]
    return y_true, y_pred


def print_metrics(metrics, title="Attack Results"):
    """Print metrics in the same table format as AttackMetricsTracker.print_metrics()."""
    print("\n" + "+" + "-" * 31 + "+" + "-" * 8 + "+")
    print("|" + f" {title}".ljust(31) + "|" + "".ljust(8) + "|")
    print("+" + "-" * 31 + "+" + "-" * 8 + "+")
    print(f"| Number of successful attacks: | {metrics['num_successful']:6d} |")
    print(f"| Number of failed attacks:     | {metrics['num_failed']:6d} |")
    print(f"| Number of skipped attacks:    | {metrics['num_skipped']:6d} |")
    print(f"| Original accuracy:            | {metrics['original_accuracy']:6.2f}% |")
    print(f"| Accuracy under attack:        | {metrics['attacked_accuracy']:6.2f}% |")
    print(f"| Attack success rate:          | {metrics['attack_success_rate']:6.2f}% |")
    print(f"| Recall (Smishing=1):          | {metrics['recall']:6.2f}% |")
    print(f"| F1 (Smishing=1):              | {metrics['f1']:6.2f}% |")
    print(f"| Average perturbed word %:     | {metrics['avg_perturbed_word_pct']:6.2f}% |")
    print(f"| Average num. words per input: | {metrics['avg_words_per_input']:6.2f} |")
    print(f"| Avg num queries:              | {metrics['avg_num_queries']:6.2f} |")
    print("+" + "-" * 31 + "+" + "-" * 8 + "+")


def print_confusion_matrix(y_true, y_pred, title="Confusion Matrix"):
    """Print confusion matrix in the same format as AttackMetricsTracker.print_confusion_matrix()."""
    if not y_true or not y_pred:
        print("No data available for confusion matrix.")
        return
    cm = sk_confusion_matrix(y_true, y_pred, labels=[0, 1])
    print("\n" + "=" * 80)
    print(title.upper())
    print("=" * 80)
    print("\n                    Predicted")
    print("                  Legitimate  Smishing")
    print(f"  Actual Legitimate    {cm[0][0]:4d}      {cm[0][1]:4d}")
    print(f"  Actual Smishing      {cm[1][0]:4d}      {cm[1][1]:4d}")
    print("\n" + "=" * 80)


def infer_attack_name_from_path(path):
    """Infer attack name from filename like '<attack>_results_YYYYMMDD_HHMMSS.json'."""
    base = os.path.basename(path)
    if "_results_" not in base:
        return "Attack"
    prefix = base.split("_results_", 1)[0]
    if not prefix:
        return "Attack"
    return prefix.replace("_", " ").title()


def resolve_paths(path=None, attack=None, results_dir="attack_results"):
    """
    Resolve input files:
    - explicit --path (single file)
    - latest file by --attack prefix
    - latest *_results_*.json if no filters
    """
    if path:
        if not os.path.isfile(path):
            raise SystemExit(f"File not found: {path}")
        return [path]

    if attack:
        pattern = os.path.join(results_dir, f"{attack.lower()}_results_*.json")
    else:
        pattern = os.path.join(results_dir, "*_results_*.json")
    files = sorted(glob.glob(pattern), key=os.path.getmtime)
    if not files:
        raise SystemExit(f"No results found for pattern: {pattern}")
    return [files[-1]]


def main():
    parser = argparse.ArgumentParser(
        description="Print attack results summary (metrics + confusion matrix) from JSON."
    )
    parser.add_argument(
        "--path",
        default=None,
        help="Path to a specific *_results_*.json file.",
    )
    parser.add_argument(
        "--attack",
        default=None,
        help="Attack name prefix filter (e.g., pruthi, bae, clare, textbugger).",
    )
    parser.add_argument(
        "--title",
        default=None,
        help="Optional custom title for printed sections.",
    )
    parser.add_argument(
        "--combine",
        action="store_true",
        help="Combine all matching files instead of using only the latest match.",
    )
    args = parser.parse_args()

    paths = resolve_paths(path=args.path, attack=args.attack)
    if args.combine and not args.path:
        if args.attack:
            pattern = os.path.join("attack_results", f"{args.attack.lower()}_results_*.json")
        else:
            pattern = os.path.join("attack_results", "*_results_*.json")
        paths = sorted(glob.glob(pattern), key=os.path.getmtime)

    entries = []
    for path in paths:
        entries.extend(load_results(path))

    if not entries:
        print("No entries in results file.")
        return

    if len(paths) == 1:
        inferred = infer_attack_name_from_path(paths[0])
        print(f"Loaded results: {paths[0]}")
    else:
        inferred = "Combined Attack"
        print(f"Combined {len(paths)} files.")

    title = args.title or f"{inferred} Results"
    metrics = compute_metrics_from_json(entries)
    print_metrics(metrics, title=title)

    y_true, y_pred = confusion_matrix_from_json(entries)
    print_confusion_matrix(y_true, y_pred, title=f"{inferred} Confusion Matrix")


if __name__ == "__main__":
    main()
